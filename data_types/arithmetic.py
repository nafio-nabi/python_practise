num_one = 10
num_two = 5

# Addition
print(num_one + num_two)

# Subtraction
print(num_one - num_two)

# Multiplication
print(num_one * num_two)

# Division
print(num_one / num_two)

# Floor division
print(num_one // num_two)

# Modulo division
print(num_one % num_two)

# Order of operations
print(num_one + num_two * 2)
print((num_one + num_two) * 2)